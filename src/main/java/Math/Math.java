public class Math {

    public static int topla(int a, int b) {
        return a + b;
    }

    public static int cix(int a, int b) {
        return a - b;
    }

    public static int vur(int a, int b) {
        return a * b;
    }

    public static int bol(int a, int b) {
        return a / b;
    }
}
