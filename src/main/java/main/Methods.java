import Math.Math;

public class Methods {

    public static int calculiator(int a, int b, int emeliyyat) {

        if (emeliyyat == 1) {
            return Math.topla(a, b);

        } else if (emeliyyat == 2) {
            return Math.cix(a, b);
        } else if (emeliyyat == 3) {
            return Math.vur(a, b);
        } else if (emeliyyat == 4) {
            return Math.bol(a, b);
        } else {
        }
        return 0;

    }

}
